#include "Crypt.hpp"

Crypting::Crypting() {};

Crypting::~Crypting() {};

std::string Crypting::encode(std::string content) {
    std::string encodedContent = content;
    return encodedContent;
}

std::string Crypting::decode(std::string content) {
    std::string decodedContent = content;
    return decodedContent;
}